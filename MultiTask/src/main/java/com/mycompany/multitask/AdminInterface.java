    private void agregarContactoDirecto() {
        try {
            String nombre = txtNombre.getText();
            String telefono = txtTelefono.getText();
            String correo = txtCorreo.getText();

            boolean exito = contactosDAO.agregarContacto(nombre, telefono, correo);
            String mensaje = exito ? "Contacto agregado exitosamente" : "Error al agregar contacto";
            JOptionPane.showMessageDialog(this, mensaje);

            if (exito) {
                txtNombre.setText("");
                txtTelefono.setText("");
                txtCorreo.setText("");
                actualizarTabla();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar contacto: " + e.getMessage());
        }
    }

    private void actualizarTabla() {
        List<String[]> contactos = contactosDAO.obtenerContactos();
        DefaultTableModel model = (DefaultTableModel) tblContactos.getModel();
        model.setRowCount(0);
        for (String[] contacto : contactos) {
            model.addRow(contacto);
        }
    }

   


    
    
    
