    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblContactos.getSelectedRow();
        if (selectedRow >= 0) {
            int id = Integer.parseInt(tblContactos.getValueAt(selectedRow, 0).toString());
            if (contactosDAO.eliminarContacto(id)) {
                JOptionPane.showMessageDialog(this, "Contacto eliminado exitosamente");
                actualizarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar contacto");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un contacto para eliminar");
        }
       
    }//GEN-LAST:event_btnEliminarActionPerformed

